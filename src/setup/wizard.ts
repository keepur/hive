#!/usr/bin/env npx tsx
/**
 * Hive Setup Wizard — interactive CLI to configure a new Hive instance.
 *
 * Saves progress after each section so you can quit and resume.
 * On resume, completed sections are detected and skipped (with option to redo).
 *
 * Usage:
 *   npm run setup
 *   npx tsx setup/setup-wizard.ts
 */

import { createInterface } from "node:readline";
import { readFileSync, writeFileSync, existsSync, readdirSync } from "node:fs";
import { execFileSync } from "node:child_process";
import { resolve, join } from "node:path";
import { stringify as toYaml, parse as parseYaml } from "yaml";
import { MongoClient } from "mongodb";
