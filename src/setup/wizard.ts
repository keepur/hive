async function doAgent(hive: Record<string, any>) {
  section("Chief of Staff Agent");

  console.log("Hive starts with a Chief of Staff — your primary agent.");
  console.log("Additional agents are created through it as needed.\n");

  const agentName = await ask("Name your Chief of Staff", "Mokie");

  // Store agent name in hive.yaml for template rendering
  if (!hive.agents) hive.agents = {};
  hive.agents["chief-of-staff"] = { name: agentName };
  saveHiveYaml(hive);

  // Seed chief-of-staff into agent_definitions via setup:seeds
  console.log("  Seeding agent definition...");
  execFileSync("npx", ["tsx", "setup/setup-seeds.ts"], { cwd: DEV_ROOT, stdio: "inherit" });

  console.log(`  ✓ ${agentName} (Chief of Staff) is ready`);
}

async function doConstitution(hive: Record<string, any>) {
  section("Constitution — Chief of Staff Permissions");

  const cosName = hive.agents?.["chief-of-staff"]?.name ?? "Chief of Staff";
  const ownerName = hive.business?.owner?.name ?? "you";
  const hasOtherAgents = Object.keys(hive.agents ?? {}).length > 1;

  console.log("The constitution defines what your agents can and can't do.");
  console.log(`Since ${cosName} is your only agent right now, we need to decide`);
  console.log("what permissions to grant.\n");

  // Initialize constitution config
  if (!hive.constitution) hive.constitution = {};

  // 1. Agent management
  console.log(`── Agent definitions (agent_definitions collection)`);
  console.log(`${cosName} can create new agents, edit their personality and config,`);
  console.log(`and manage the team roster.\n`);
  hive.constitution.cosCanManageAgents = true;
  console.log(`  ✓ ${cosName} can manage agent definitions\n`);

  // 2. External communications (hard gate)
  console.log(`\n── External communications (email, SMS)`);
  console.log(`This is a hard gate — when off, email and SMS tools are completely`);
  console.log(`removed from all agents. Not just a policy, they literally can't send.\n`);
  console.log(`Default: OFF. You can enable later by setting EXTERNAL_COMMS_ENABLED=true`);
  console.log(`in .env and restarting Hive.\n`);
  hive.constitution.cosCanContactExternal = await confirm(`Enable external communications now?`, false);

  // Load env to set the hard gate flag
  const extEnv = loadEnv();
  if (hive.constitution.cosCanContactExternal) {
    extEnv.EXTERNAL_COMMS_ENABLED = "true";
    console.log(`  ✓ External comms enabled — email/SMS tools available`);
    console.log(`    (sensitive topics still escalate to ${ownerName} per constitution)`);
  } else {
    extEnv.EXTERNAL_COMMS_ENABLED = "";
    console.log(`  ✗ External comms disabled — no agent can send email or SMS`);
    console.log(`    To enable later: set EXTERNAL_COMMS_ENABLED=true in .env`);
  }
  saveEnv(extEnv);

  // 3. Summary
  console.log("\n── Summary ─────────────────────────────────────");
  console.log(`  Agent management:       ✓ yes`);
  console.log(`  External communications: ${hive.constitution.cosCanContactExternal ? "✓ yes" : "✗ no"}`);
  console.log(`  Hive build & deploy:    ✗ no (external provisioning only)`);
  console.log(`  Hive source code:       ✗ no (external provisioning only)`);

  saveHiveYaml(hive);
  console.log("\n  ✓ Constitution preferences saved (hive.yaml)");
}

async function doMemory(hive: Record<string, any>, templatesDir: string) {
  const mongoUri = process.env.MONGODB_URI || "mongodb://localhost:27017";
  const mongoDb = process.env.MONGODB_DB || "hive";

  console.log("Seeding shared memory to MongoDB...");

  try {
    const client = new MongoClient(mongoUri);
    await client.connect();
    const db = client.db(mongoDb);
    const memory = db.collection("memory");

    // Ensure index
    await memory.createIndex({ path: 1 }, { unique: true });

    // Business context
    const biz = hive.business ?? {};
    const contextMd = [
      `# Business Context\n`,
      `**Company:** ${biz.name ?? ""}`,
      `**About:** ${biz.description ?? ""}`,
      biz.location ? `**Location:** ${biz.location}` : "",
      biz.timezone ? `**Timezone:** ${biz.timezone}` : "",
      biz.businessHours ? `**Business Hours:** ${biz.businessHours}${biz.timezone ? ` (${biz.timezone})` : ""}` : "",
      `**Owner:** ${biz.owner?.name ?? ""} (${biz.owner?.role ?? ""})`,
      "",
      "## Additional Context",
      "(Add more details about your business here — products, services, team, etc.)",
    ]
      .filter(Boolean)
      .join("\n");

    await memory.updateOne(
      { path: "shared/business-context.md" },
      { $set: { content: contextMd, updatedAt: new Date(), updatedBy: "setup-wizard" } },
      { upsert: true },
    );
    console.log("  ✓ shared/business-context.md");

    // Constitution
    const constitutionTplPath = join(templatesDir, "constitution.md.tpl");
    if (existsSync(constitutionTplPath)) {
      const constitutionTpl = readFileSync(constitutionTplPath, "utf-8");

      const team: Record<string, string> = {};
      for (const [id, def] of Object.entries(hive.agents ?? {})) {
        team[id] = (def as any).name ?? id;
      }

      const constitutionCtx = { business: biz, team };
      const content = renderTemplate(constitutionTpl, constitutionCtx);

      await memory.updateOne(
        { path: "shared/constitution.md" },
        { $set: { content, updatedAt: new Date(), updatedBy: "setup-wizard" } },
        { upsert: true },
      );
      console.log("  ✓ shared/constitution.md");
    }

    await client.close();
    console.log("  ✓ Memory seeded to MongoDB");
  } catch (err) {
    console.log(`  ⚠ Failed to seed memory: ${err}`);
    console.log("  Make sure MongoDB is running (brew services start mongodb-community)");
  }
}

